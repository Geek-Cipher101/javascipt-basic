 const month = "march"
 switch (month) {
    case "jan":
        console.log('January');
        break;
    case "feb":
        console.log('February');
        break;
    case "march":
        console.log('March');
        break;
    case "april":
        console.log('April');
        break;
    default:
        console.log('Invalid month');
        break;
}

